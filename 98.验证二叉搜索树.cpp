//     TreeNode *right;
//     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
// };

class Solution {
public:
    bool doit(TreeNode* root, long low, long high) {
        if (!root) return true;
        if (root->val > low && root->val < high) {
            return doit(root->left, low, root->val) && doit(root->right, root->val, high);
        } else {
            return false;
        }
    }
    bool isValidBST(TreeNode* root) {
        return doit(root, LONG_MIN, LONG_MAX);
    }
};
